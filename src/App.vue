<template>
  <div id="app">
    <UiContainer>
      <div id="nav">
        <router-link to="/">Homes</router-link>
        <div v-if="isLoggedIn">
          <div>Signed in as {{ email }}</div>
          <button @click="logout">Logout</button>
        </div>
      </div>
      <router-view />
    </UiContainer>
  </div>
</template>

<script lang="ts">
import firebase from "firebase";
import { defineComponent, computed, reactive } from "@vue/composition-api";

import {
  provideStore,
  useUserStore,
  useTaskStore,
} from "./composables/use_store";
import { provideRouter, provideRoute } from "@/composables/user_router";
import { initializeFirebaseAuth } from "@/lib/firebase";

import UiContainer from "@/components/ui/Container.vue";

const App = defineComponent({
  name: "App",
  components: {
    UiContainer,
  },
  setup(_, { root: { $store, $router } }) {
    provideStore($store);
    provideRouter($router);

    const routeData = reactive({ params: {}, query: {} });
    $router.afterEach((route) => {
      routeData.params = route.params;
      routeData.query = route.query;
    });
    provideRoute(routeData);

    const userStore = useUserStore();
    const taskStore = useTaskStore();
    initializeFirebaseAuth(userStore, taskStore);

    const email = computed(() => userStore.email);
    const isLoggedIn = computed(() => userStore.isLoggedIn);

    const logout = async () => {
      await firebase.auth().signOut();
      userStore.logout();
    };

    return {
      email,
      isLoggedIn,
      logout,
    };
  },
});
export default App;
</script>

